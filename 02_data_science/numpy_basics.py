import numpy as np # импортирую библеотеку NumPy

# создаю массив чисел
numbers = np.array([10, 20, 30, 40, 50])

# выдаю массив
print("Массив чисел:", numbers)

# базовые вычисления
print("Сумма:", numbers.sum())          # сумма всех элементов
print("Среднее:", numbers.mean())       # среднее значение
print("Максимум:", numbers.max())       # максимальное значение
print("Минимум:", numbers.min())        # минимальное значение

# создаю массив из нулей
zeros = np.zeros(5)
print("Массив из нулей:", zeros)

# создаю массив из единиц
ones = np.ones(5)
print("Массив из единиц:", ones)

# создаю последовательность чисел от 1 до 10
sequence = np.arange(1, 11)
print("Последовательность от 1 до 10:", sequence)