# –ö–ª—é—á–∏ —É–Ω–∏–∫–∞–ª—å–Ω—ã, –∞ –∑–Ω–∞—á–µ–Ω–∏—è –º–æ–≥—É—Ç –ø–æ–≤—Ç–æ—Ä—è—Ç—å—Å—è.

def print_profile(profile):
    """–ê–∫–∫—É—Ä–∞—Ç–Ω–æ –≤—ã–≤–æ–¥–∏–º –¥–∞–Ω–Ω—ã–µ –∏–∑ —Å–ª–æ–≤–∞—Ä—è –ø—Ä–æ—Ñ–∏–ª—è."""
    print("\n=== –ü—Ä–æ—Ñ–∏–ª—å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è ===")
    print(f"–ò–º—è: {profile.get('name', '–ù–µ —É–∫–∞–∑–∞–Ω–æ')}")
    print(f"–í–æ–∑—Ä–∞—Å—Ç: {profile.get('age', '–ù–µ —É–∫–∞–∑–∞–Ω')}")
    print(f"–ì–æ—Ä–æ–¥: {profile.get('city', '–ù–µ —É–∫–∞–∑–∞–Ω')}")
    skills = profile.get("skills", [])
    if skills:
        print("–ù–∞–≤—ã–∫–∏:", ", ".join(skills))
    else:
        print("–ù–∞–≤—ã–∫–∏: –ø–æ–∫–∞ –ø—É—Å—Ç–æ")


# –ü—Ä–∏–º–µ—Ä –ø—Ä–æ—Å—Ç–æ–≥–æ —Å–ª–æ–≤–∞—Ä—è:
