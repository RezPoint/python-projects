# lists_examples.py
# Пример работы со списками в Python

# Создание списка
fruits = ["яблоко", "банан", "вишня"]
print("Список фруктов:", fruits)

# Доступ к элементам по индексу
print("Первый фрукт:", fruits[0])  # индекс 0 = первый элемент
print("Последний фрукт:", fruits[-1])  # -1 = последний элемент

# Добавление элементов
fruits.append("апельсин")  # добавляем в конец
print("После добавления апельсина:", fruits)

# Вставка элемента на определенную позицию
fruits.insert(1, "киви")  # вставим "киви" на индекс 1
print("После вставки киви:", fruits)

# Удаление элементов
fruits.remove("банан")  # удаляем по значению
print("После удаления банана:", fruits)

# Получение элемента и его удаление одновременно
last_fruit = fruits.pop()  # удаляет последний элемент и возвращает его
print("Удалили фрукт:", last_fruit)
print("Список после pop:", fruits)

# Длина списка
print("Количество фруктов в списке:", len(fruits))

# Перебор элементов списка
print("Перебор всех фруктов:")
for fruit in fruits:
    print("-", fruit)
